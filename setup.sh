# AWS commands

# create an AWS bucket, with name_of_bucket replacing 'frequently-modulated'
aws s3 mb s3://frequently-modulated

# get vpcId from
aws ec2 describe-vpcs

# get RouteTableId from
aws ec2 describe-route-tables

# create S3 gateway endpoint (aka VPC gateway)
aws ec2 create-vpc-endpoint --vpc-id <vpcId from above> --service-name com.amazonaws.us-east-1.s3 --route-table-ids <RouteTableId>

# create IAM service role that can be assumed by Glue:
aws iam create-role --role-name my-glue-service-role --assume-role-policy-document '{
    "Version": "2012-10-17",
    "Statement": [
        {
            "Effect": "Allow",
            "Principal": {
                "Service": "glue.amazonaws.com"
            },
            "Action": "sts:AssumeRole"
        }
    ]
}'

# grant Glue privileges to the S3 bucket, with name_of_bucket replacing 'frequently-modulated'
aws iam put-role-policy --role-name my-glue-service-role --policy-name S3Access --policy-document '{
    "Version": "2012-10-17",
    "Statement": [
        {
            "Sid": "ListObjectsInBucket",
            "Effect": "Allow",
            "Action": [
                "s3:ListBucket"
            ],
            "Resource": [
                "arn:aws:s3:::frequently-modulated"
            ]
        },
        {
            "Sid": "AllObjectActions",
            "Effect": "Allow",
            "Action": "s3:*Object",
            "Resource": [
                "arn:aws:s3:::frequently-modulated/*"
            ]
        }
    ]
}'

